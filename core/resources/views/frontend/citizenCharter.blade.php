							<li>গ. টেলিফোনে প্রাপ্ত সংবাদের ভিত্তিতে প্রয়োজনীয় ব্যবস্থা গ্রহণ করবেন।</li>
						</ul>

						<p>১৫। সকল থানায় মেট্রোপলিটন এলাকার জন্য কমিশনার, অতিরিক্ত কমিশনার, সংশ্লিষ্ট জয়েন্ট কমিশনার, ডিসি, এডিসি এবং জোনাল এসি এবং জেলার জন্য পুলিশ সুপার, অতিরিক্ত পুলিশ সুপার, এএসপি (হেডকোয়ার্টার্স), সংশ্লিষ্ট সার্কেল এএসপি এবং থানার ভারপ্রাপ্ত কর্মকর্তা টেলিফোন নম্বর প্রকাশ্য স্থানে প্রদর্শিত হবে। </p>

						<p>১৬। মেট্রো পলিটন ও জেলায় কর্তব্যরত সকল পর্যায়ের অফিসারগণ প্রতি কারযদিবসে নির্ধারিত সময়ে সকল সাহায্যপ্রার্থীকে সাহায্য প্রদান করবে। </p>

