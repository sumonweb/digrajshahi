						<p>১৮। উর্দ্ধতন পুলিশ কর্মকর্তাগণ নিয়মিত কম্যুনিটির সহিত অপরাধ দমনমূলক/জনসংযোগমূলক সভা করবেন এবং সামাজিক সমস্যাসমূহের আইনগত সমাধানের প্রয়াস চালাবেন।</p>

						<p>১৯। বিদেশে চাকুরী/উচ্চ শিক্ষার জন্য গমনেচ্ছু প্রার্থীদের পুলিশ ক্লিয়ারেন্স  সার্টিফিকেট প্রদান করবেন।</p>

						<p>২০। ব্যাংক হতে কোন প্রতিষ্ঠান অধিক পরিমাণ টাকা উত্তোলন করলে উক্ত টাকা নিরাপদে নেওয়ার জন্য চাহিদা অনুযায়ী পুীলশ এস্কর্টের ব্যবস্থা করা হবে।</p>

						<p>২১। মেট্রোপলিটন শহর/জেলা শহরে যানবাহন নিয়ন্ত্রণে ট্রাফিক বিভাগ, ট্রাফিক সংশ্লিষ্ট কি কি সেবা প্রদান করছে তা প্রকাশ্য স্থানে প্রদর্শিত হবে।</p>
