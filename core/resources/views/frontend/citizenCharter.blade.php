						<p>১৪। থানা হতে বর্ণিত আইনগত সহযোগিতা না পাওয়া গেলে বা কোন পুলিশ সদস্যের বিরুদ্ধে কোন অভিযোগ থাকলে উর্ধ্বতন কর্তৃপক্ষ বরাবর অভিযোগ দাখিল করা যাবে। সেইক্ষেত্রে উর্ধ্বতন কর্তৃপক্ষঃ</p>
